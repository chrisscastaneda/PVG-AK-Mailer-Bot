                <h3 class="text-primary">Vote YES to Save Metro</h3>
                <p>King County Metro transit is essential to our economy, our environment, and our communities. More than 400,000 people rely on Metro to get to school, work, and medical appointments everyday. Yet without action, Metro faces drastic cuts to bus service that would strand thousands of seniors, parents, students, people with disabilities, and others without any transportation options. Proposition 1 will prevent these cuts by filling the 17 percent budget gap with an additional $40 car tab fee (or $20 less for low-income car owners) and a 0.1 percent sales tax increase. In addition, Proposition 1 will temporarily reduce the new low-income bus fare to $1.25. If we do not approve Proposition 1, these cuts will impact 80 percent of King County bus riders and will add 30,000 more cars on the road. We need to approve Proposition 1 and save transit options for thousands of King County families.</p>
