                <p class="writeup">Our schools are the foundation of our children's futures. The Lake Washington School District is proposing a $404 million bond to address enrollment growth and overcrowded classrooms. It will be used to build three new elementary schools (two in Redmond and one in Kirkland) and a new middle school in Redmond. Juanita High School in Kirkland would be rebuilt to house a larger number of students, and a 600-student STEM-focused high school would also be built on that campus. Lake Washington High School would also be expanded. The cost to taxpayers is estimated at 25 cents per thousand dollars of assessed home value, or $124 per year. Vote to approve the Lake Washington school bond.
